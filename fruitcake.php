		<h2>Rules & Guidelines</h2>
			<div class="row">
				<div class="six columns alpha">
				<h3>Festival Rules</h3>
					<ol>
						<li>Films should be 10-second, 30-second, or 60-second in length</li>
						<li>You may submit more than one film</li>
						<li>Each film must adhere to one of the holiday-based themes to be revealed on December 1st</li>
						<li>This is a holiday film festival, so please, PG rated themes only</li>
						<li>TIFN reserves the right to refuse screening of any film based on content</li>
						<li>If you are unable to join us on Sat December 1st, you are still welcome to make and submit film(s)</li>
						<li>ALL entries must be received by 11:59 p.m. on Monday, December 3rd.</li>
					</ol>
				</div>
				<div class="ten columns omega offset-by-one">
					<h3>Submission Guidelines</h3>
					<ol>
						<li>Films can be created with any camera and editing program</li>
						<li>As long as your file plays on a Mac or PC computer or your smartphone or tablet, we can screen it</li>
						<li>For best results on the big screen, create the film as the highest resolution possible e.g. “HD or 1080p”</li>
						<li>Your audio needs to be clear – not too loud, not too soft</li>
						<li>For the technical geeks and/or the professional filmmakers – a 24 frames per second, 1080p file (e.g. ProRes or Avid 4:2:2) will look best</li>
						<li>For the wannabee Kubricks out there, a 2K or 4K master in Flat or Scope aspect ratio with 5.1 surround will play best</li>
						<li>If you don’t know what any of this means, don’t worry about it, just make your film and have fun doing it!</li>
					</ol>
					<br />
				</div>
